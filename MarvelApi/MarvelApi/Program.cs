using MarvelApi.Interface;
using MarvelApi.Mappings;
using MarvelApi.Rest;
using MarvelApi.Service;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();

builder.Services.AddSingleton<IHeroisService, HeroiService>();
builder.Services.AddSingleton<IMarvelApi, MarvelApiRest>();

builder.Services.AddAutoMapper(typeof(HeroiMapping));

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
